public class Do_While_Loop {
    public static void main(String[] args){

        //Write Hello World With the help of Do While Loop

        int i = 0;
        do {
            System.out.println("Hello World");
            i++;
        }while (i>5);

        //One of the major benefit or advantage of using a do while loop is
        // that at least one time it will execute whether the condition is correct or it is not correct
    }
}
